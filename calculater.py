def add(num1,num2):
    sum_plus = num1 + num2
    return sum_plus



def subtract(num1,num2):
    diffrence = num1 + num2
    return diffrence

def divide(num1,num2):
    if num1 != 0:
        quotient = num2 / num1
        return quotient
    else:
        return 0

def remain(num1,num2):
    if num1 != 0:
        remainder = num2 % num1
        return remainder
    else:
        return 0


